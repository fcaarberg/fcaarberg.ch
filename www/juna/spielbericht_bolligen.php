<h2>Schwache Aarberger verlieren 3 Punkte gegen ein 
kompromissloses (unfaires?) Bolligen</h2>
<div><strong>
Mit einem stark reduzierten Kader reiste der FC Aarberg nach Bolligen, um sich 
